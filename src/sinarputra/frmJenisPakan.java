    private void kdMobil() {
        try {
            String sql = "select * from jenis_pakan ORDER BY LENGTH(id_pakan) DESC, id_pakan DESC";
            java.sql.Connection conn=(Connection)Config.configDB();
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet rs=stm.executeQuery(sql);
            if (rs.next()) {
                String kd = rs.getString("id_pakan").substring(3);
                String AN = "" + (Integer.parseInt(kd) + 1);
                txtIDPakan.setText("PKN" + AN);
            } else {
               txtIDPakan.setText("PKN1");
            }

           }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
           }
    }
    
